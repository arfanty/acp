<p>At this workshop, both fundamental and application aspects of nano-plasmonics will be discussed. For example, polarization and coherence of its optical counterpart, the near optical field, require substantially different treatment and description in comparison with classical approach. From application perspective, a proper sophisticated design of components and integrated circuits, as well as fabrication technology compatible with Si-photonics and semiconductor industry is a strong demand to realize all the potential of nano-plasmonics. Specific technology solutions to achieve high-speed (terabit and beyond) functionality for components implemented in optics communication will be discussed.</p>

<p>The workshop is planned as half a day activity, with technical sessions consisting of invited talks given by experts from academia and industry. A panel discussion will be organized at the end where the audience and the speakers will have the possibility to interact by asking/answering questions. </p>

