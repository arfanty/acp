Optical fibre communication is by far the least energy-consuming and least costly technique to offer ultra-high capacity for telecommunication networks. It has also been considered as a promising transmission technology for future datacentre applications. This workshop will focus on the main trends in optical interconnection networks for datacentre applications. Possible future research directions will be discussed in order to achieve the following challenging goals:</p>
<p>1) Ultra-high capacity for the datacentre to support the needs of the “networked society”.</p>
<p>2) Improved energy-efficiency throughout all parts of the datacentre network.</p>
